package clases;

public class variables {
    String Nombre;
    String Apellido;
    int Dia;
    int Mes;
    int Año;
    String TD;
    String CE;
    int ND;
    long TL;
    String MTDP;
    int TC;
  
    public variables(String Nombre, String Apellido, int Dia, int Mes, int Año, String TD, String CE, int ND, long TL, String MTDP, int TC) {
        this.Nombre = Nombre;
        this.Apellido = Apellido;
        this.Dia = Dia;
        this.Mes = Mes;
        this.Año = Año;
        this.TD = TD;
        this.CE = CE;
        this.ND = ND;
        this.TL = TL;
        this.MTDP = MTDP;
        this.TC = TC;
    }


    

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getApellido() {
        return Apellido;
    }

    public void setApellido(String Apellido) {
        this.Apellido = Apellido;
    }

    public int getDia() {
        return Dia;
    }

    public void setDia(int Dia) {
        this.Dia = Dia;
    }

    public int getMes() {
        return Mes;
    }

    public void setMes(int Mes) {
        this.Mes = Mes;
    }

    public int getAño() {
        return Año;
    }

    public void setAño(int Año) {
        this.Año = Año;
    }

    public String getTD() {
        return TD;
    }

    public void setTD(String TD) {
        this.TD = TD;
    }

    public String getCE() {
        return CE;
    }

    public void setCE(String CE) {
        this.CE = CE;
    }

    public int getND() {
        return ND;
    }

    public void setND(int ND) {
        this.ND = ND;
    }

    public long getTL() {
        return TL;
    }

    public void setTL(long TL) {
        this.TL = TL;
    }

    

    public String getMTDP() {
        return MTDP;
    }

    public void setMTDP(String MTDP) {
        this.MTDP = MTDP;
    }

    public int getTC() {
        return TC;
    }

    public void setTC(int TC) {
        this.TC = TC;
    }
   
            
    
    
    
}
